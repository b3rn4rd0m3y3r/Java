// Class Declaration
public class Retangulo {
	// Instance Variables
	int Lado1;
	int Lado2;

	public Retangulo(){
		// Construtor simples
		}
	
	public Retangulo(int pLado1, int pLado2){
		Lado1 = pLado1;
		Lado2 = pLado2;
		}
	
	public int Area(){
		return (Lado1 * Lado2);
		}

}